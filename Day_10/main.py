#string Function
str1="python is dynamic type language" 
# upper 
print("Using Upper Function :-",str1.upper())
# lower
print("Using Lower Function :-",str1.lower())
# isupper()
print("using isupper function :- ",str1.isupper())
# title()
print("using titile function :-",str1.title())
# istitle()
print("using istitle function :-",str1.istitle())
# islower()
print("Using isLower Function :-",str1.islower())
# split()
print("Using split Function :-",str1.split())
# strip()
print("using strip Function :-",str1.strip())
# count()
print("using count function :-",str1.count("p"))
# find()
print("using find function :-",str1.find("a"))
# index()
print("using index function :-",str1.index("python"))
# partition()
print("Using Partition Function :-",str1.partition("s"))


